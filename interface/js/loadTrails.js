let trailLinks = []
let trailsDiv = document.getElementById("trailsDiv");

for (let i = 0; i < trailInfo.length; i++) {
  let trailDiv = document.createElement("div")
  let trailLink = document.createElement("a");
  trailLink.innerHTML = trailInfo[i].trailName;
  trailLink.href = "./trailDetail.html";
  trailLink.className = "link";
  trailLink.id = String(i);
  trailLink.addEventListener("click", function(event) {
    localStorage.setItem("trail", JSON.stringify(trailInfo[Number(event.target.id)]));
  });
  trailLink.style.marginLeft = "16px";
  trailDiv.className = "trailLinkDiv";
  trailDiv.appendChild(trailLink);
  trailLinks.push(trailDiv);
}

for (let i = 0; i < trailLinks.length; i++) {
  trailsDiv.appendChild(trailLinks[i]);
}
